package DaemonThreads;

import java.util.Date;

class Event
{
    private Date date;
    private String event;

    public Date getDate()
    {
        return this.date;
    }

    public void setDate(Date date)
    {
        this.date = date;
    }

    public String getEvent()
    {
        return this.event;
    }

    public void setEvent(String event)
    {
        this.event = event;
    }

}
